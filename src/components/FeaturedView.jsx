import React, { useRef, useState, useEffect } from "react";
import { motion } from "framer-motion";
import { useMenu } from "../context/MenuContext";
import ViewsHeader from "./ViewsHeader";
import Modal from "./UI/Modal";
import RatingSystem from "./RatingSystem";

function FeaturedView() {
  const containerRef = useRef(null);
  const [isModalOpen, setIsModalOpen] = useState(false);
  const openModal = () => setIsModalOpen(true);
  const closeModal = () => setIsModalOpen(false);
  const [currentView, setCurrentView] = useState(0);
  const [currentCategory, setCurrentCategory] = useState('Lasagna'); // Ensure initial category matches the database
  const { menu, loading, error } = useMenu();

  const scrollToSection = (index) => {
    setCurrentView(index);
    const section = containerRef.current.children[index];
    section.scrollIntoView({ behavior: "smooth", inline: "start" });
  };

  const handleCategoryChange = (category) => {
    setCurrentCategory(category);
    setCurrentView(0);  // Resetting current view
    containerRef.current.scrollTo({ left: 0, behavior: 'smooth' });
  };

  const filteredMenuItems = menu.filter(item => item.category.toLowerCase() === currentCategory.toLowerCase());
  console.log('Filtered Menu Items:', filteredMenuItems);

  if (loading) {
    return <div>Loading menu items...</div>;
  }

  if (error) {
    return <div>Error loading menu items: {error}</div>;
  }

  return (
    <main className="h-full snap-center">
      <section className="h-full flex flex-col justify-between relative">
        <ViewsHeader viewName="Featured" />
        <div className="w-full h-full flex flex-col justify-between items-center relative">
          <div className="w-full box-border flex flex-col items-center justify-center min-h-[85%] gap-6 relative">
            <div
              className="flex items-center justify-start gap-3 overflow-auto snap-x snap-mandatory w-full hide-scroll h-full px-2"
              ref={containerRef}
            >
              {filteredMenuItems.map((item, index) => (
                <section
                  key={item.id}
                  className="flex-none items-center my-auto w-[95%] md:w-1/2 lg:w-1/5 h-full snap-center hide-scroll box-border drop-shadow-lg"
                >
                  <div className="bg-mYellow flex flex-col items-center justify-center p-2 gap-4  rounded-md">
                    <div className="flex items-center justify-center gap-2 h-full">
                      <div className="basis-4/6 h-full flex items-center justify-center">
                        <img
                          className="object-cover h-full rounded-lg shadow-4xl"
                          src={item.imageUrls[0] || 'default-image-url'}
                          alt={item.name}
                        />
                      </div>
                      <div className="flex flex-col items-center justify-center basis-2/6 gap-1 aspect-square h-full">
                        <div className="h-full shadow-4xl w-full">
                          <img
                            className="aspect-square object-cover h-full rounded-lg shadow-4xl"
                            src={item.imageUrls[1] || item.imageUrls[0] || 'default-image-url'}
                            alt={item.name}
                          />
                        </div>
                        <div className="h-full">
                          <img
                            className="aspect-square h-full object-cover rounded-lg shadow-4xl"
                            src={item.imageUrls[2] || item.imageUrls[0] || 'default-image-url'}
                            alt={item.name}
                          />
                        </div>
                        <div className="h-full">
                          <img
                            className="aspect-square object-cover h-full rounded-lg shadow-4xl"
                            src={item.imageUrls[3] || item.imageUrls[0] || 'default-image-url'}
                            alt={item.name}
                          />
                        </div>
                      </div>
                    </div>
                    <div className="p-3 text-left bg-white rounded-lg w-full flex flex-col gap-1 justify-center">
                      <div className="text-xl line-clamp-1 font-bold text-mRed b">
                        {item.name}
                      </div>
                      <div className="text-sm px-1 line-clamp-2 leading-7 min-h-16">
                        {item.description}
                      </div>
                    </div>
                    <div className="flex flex-wrap items-center justify-beween w-full">
                      <div className="basis-4/6 rounded-sm">
                        {item && (
                          <div className="flex items-center justify-start">
                            <ul className="p-2 flex gap-2 bg-mGreen rounded-md">
                              {item.dietaryIcons.includes("vegetarian") && (
                                <li className="text-mGreen bg-white rounded-full aspect-square w-8 flex items-center justify-center">
                                  <ion-icon name="leaf-outline"></ion-icon>
                                </li>
                              )}
                              {item.dietaryIcons.includes("special") && (
                                <li className="text-mGreen bg-white rounded-full aspect-square w-8 flex items-center justify-center">
                                  <ion-icon name="ribbon-outline"></ion-icon>
                                </li>
                              )}
                              {item.dietaryIcons.includes("topRated") && (
                                <li className="text-mGreen bg-white rounded-full aspect-square w-8 flex items-center justify-center">
                                  <ion-icon name="star-outline"></ion-icon>
                                </li>
                              )}
                              {item.dietaryIcons.includes("g'sFavorite") && (
                                <li className="text-mGreen bg-white rounded-full aspect-square w-8 flex items-center justify-center">
                                  <ion-icon name="heart-outline"></ion-icon>
                                </li>
                              )}
                            </ul>
                          </div>
                        )}
                      </div>
                      <motion.button
                        className="bg-white px-3 rounded-md basis-2/6 text-center"
                        whileHover={{ scale: 1.1 }}
                        whileTap={{ scale: 0.9 }}
                      >
                        <div className="flex gap-2 rounded-md items-center justify-center p-2">
                          <div className="h-8 text-md flex items-center justify-center text-mGreen" onClick={openModal}>
                            Rate Me ❤️
                          </div>
                        </div>
                      </motion.button>
                      <Modal isOpen={isModalOpen} onClose={closeModal}>
                        <RatingSystem/>
                      </Modal>
                    </div>
                  </div>
                </section>
              ))}
            </div>
            <div className="flex justify-center items-center gap-3 bg-white w-full">
              {filteredMenuItems.map((_, index) => (
                <button
                  key={index}
                  onClick={() => scrollToSection(index)}
                  className={`w-4 h-4 rounded-full ${
                    index === currentView ? "bg-mGreen" : "bg-gray-300"
                  }`}
                  aria-label={`Go to section ${index + 1}`}
                ></button>
              ))}
            </div>
          </div>
          <motion.button
            whileHover={{ scale: 1.1 }}
            whileTap={{ scale: 0.9 }}
            className="text-base bg-white rounded-full aspect-square w-6 flex items-center justify-center absolute bottom-24 right-8 drop-shadow-lg"
          >
            <ion-icon size="large" name="information-circle-outline"></ion-icon>
          </motion.button>
          <div className="flex container mx-auto items-center justify-center gap-4 h-[10%] bg-white px-6">
            <motion.button
              className="rounded-md bg-mRed text-white px-3 py-2 basis-1/3"
              onClick={() => handleCategoryChange('Lasagna')}
              whileHover={{ scale: 1.1 }}
              whileTap={{ scale: 0.9 }}
            >
              Lasagnas
            </motion.button>
            <motion.button
              className="rounded-md bg-mRed text-white px-3 py-2 basis-1/3"
              onClick={() => handleCategoryChange('Panini')}
              whileHover={{ scale: 1.1 }}
              whileTap={{ scale: 0.9 }}
            >
              Paninis
            </motion.button>
            <motion.button
              className="rounded-md bg-mRed text-white px-3 py-2 basis-1/3"
              onClick={() => handleCategoryChange('Pasta')}
              whileHover={{ scale: 1.1 }}
              whileTap={{ scale: 0.9 }}
            >
              Pastas
            </motion.button>
          </div>
        </div>
      </section>
    </main>
  );
}

export default FeaturedView;
